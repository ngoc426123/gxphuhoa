			<p>Cách nay hơn nửa thế kỷ, vào năm 1958, có 11 gia đình công giáo gốc địa phận Bắc Ninh đến xã Phú Thọ Hòa, quận Tân Bình sinh sống, và xin gia nhập giáo xứ Phú Bình. Một năm sau, với con số hơn 20 gia đình, cha cố Tôma Phạm Ngọc Biểu – chánh xứ Phú Bình – cho lập họ đạo mới, lấy tên là Họ Phú Hòa.</p>
			<p>Phú Hòa thật là vùng “đất lành chim đậu”, ngày càng có nhiều gia đình tựu về đây lập nghiệp. Năm 1971 giáo họ mua được lô đất diện tích hơn 300 mét vuông. Sau 4 tháng nỗ lực xây dựng, ngày 01-5-1973, Đức Tổng Giám mục PhaoLô Nguyễn Văn Bình và Đức cha Phụ tá Phanxicô Xaviê Trần Thanh Khâm dâng Thánh lễ khánh thành Nguyện đường Phú Hòa.</p>
			<p>Phú Hòa có cộng đồng người Hoa sinh sống. Năm 1973 giáo họ Phú Hòa được linh mục Polvala Hòa (người Pháp), đến giúp mục vụ cho bổn đạo người Hoa. Ngài dâng thánh lễ bằng tiếng Hoa và tiếng Việt. Cộng đoàn Phú Hòa trở nên thật sinh động. Người Việt và Hoa đoàn kết yêu thương. Rất tiếc, đến năm 1975, vì tình hình chung, cha Polvala phải rời Phú Hòa.</p>
			<p>Sau 15 năm xây dựng, nhà thờ đã xuống cấp, và được đại trùng tu. Ngày 27-9-1991, một bước ngoặt mới của Phú Hòa: Đức Tổng Giám mục Phaolô Nguyễn Văn Bình ký quyết định tách họ đạo Phú Hòa ra khỏi giáo xứ Phú Bình, nâng Phú Hòa lên thành Giáo xứ, tạm giao Phú Hòa cho cha Hạt trưởng Tân Sơn Nhì phụ trách đến khi có linh mục chánh xứ. Cha cố Đaminh Vũ Nguyên Thiều – Hạt trưởng Tân Sơn Nhì – kiêm nhiệm Phú Hòa không xuể. Vì thế, cha Giuse Đinh Quang Thịnh, cha Đaminh Trần Đức Công, cha Giuse Nguyễn văn Thanh được giao đặc trách giáo xứ Phú Hòa. Giáo xứ cũng được sự giúp đỡ tận tình của các Dì Dòng Mến Thánh Giá Tân Việt.</p>
			<p class="img-left"><img src="<?php echo get_template_directory_uri() ?>/images/cha-huy.png" alt=""></p>
			<p>Ngày 04-10-2007, linh mục Antôn Mai Đức Huy được Đức Hồng Y Gioan Baotixita Phạm Minh Mẫn bổ nhiệm làm chánh xứ tiên khởi của giáo xứ Phú Hòa. Giáo xứ Phú Hòa được trở về lại giáo hạt Phú Thọ. Sau khi nhận giáo xứ, cha Antôn đã đi thăm tất cả các gia đình trong giáo xứ, xem xét các sinh hoạt của giáo dân, đưa các sinh hoạt mục vụ vào nền nếp. Cha cho bầu lại Hội đồng Mục vụ giáo xứ. Giáo xứ Phú Hòa hiện nay có 617 gia đình với 2.452 giáo dân, thuộc các phường Phú Trung, Hòa Thạnh, quận Tân Phú và phường 10, quận Tân Bình.</p>
			<p>Hơn 50 năm hình thành và phát triển, Phú Hòa ngày một ổn định vững vàng trong tình yêu quan phòng của Chúa. Mọi người trong giáo xứ cũng ghi nhớ những công ơn lớn lao của các bậc tiền nhân đã hy sinh xây dựng giáo xứ, và biết bao đóng góp quảng đại của các ân nhân xa gần, cũng như của từng gia đình trong giáo xứ. Giáo dân Phú Hòa sẽ luôn phát huy những đóng góp quý báu ấy để củng cố đời sống của giáo xứ ngày càng lớn mạnh hơn.</p>
			<p>Nhìn lại những chặng đường đã qua, cộng đoàn giáo xứ Phú Hòa có được như hôm nay chính là nhờ hồng ân bao la của Chúa. Xin hiệp lời hân hoan tạ ơn Chúa, vì: “Tất cả là hồng ân”.</p>
